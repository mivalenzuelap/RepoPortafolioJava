    private void jButtonInicioSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInicioSesionActionPerformed
        String pass = new String(jPasswordFieldPass.getPassword());
        if (UsuarioController.validacionUsuario(jTextFieldUsuario.getText(), pass)) {
            localId=UsuarioController.idLocal(jTextFieldUsuario.getText(), pass);
            tipoUsuario=UsuarioController.idTipo(jTextFieldUsuario.getText(), pass);
            Menu menu = new Menu();
            menu.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Los datos ingresados no son validos");
        }
    }//GEN-LAST:event_jButtonInicioSesionActionPerformed

